// ===============================
// PENGANTAR C++
// ===============================

// Apa itu C++? (Versi super sederhana)
// C++ adalah bahasa untuk memberi perintah ke komputer supaya komputer
// bisa bekerja dengan cepat dan kuat.

// Dengan C++, kita bisa membuat:
// - Program
// - Aplikasi
// - Game
// - Sistem yang butuh kecepatan tinggi

// Bayangkan begini:
// Komputer itu mesin,
// C++ adalah bahasa instruksi supaya mesin itu tahu harus ngapain.


// ===============================
// Siapa yang membuat C++?
// ===============================

// C++ dibuat oleh Bjarne Stroustrup.

// Awalnya, dia mengambil bahasa C, lalu:
// - Ditambah fitur
// - Dibuat lebih rapi
// - Bisa dipakai untuk program besar

// Jadilah C++.


// ===============================
// Kenapa C++ itu kuat?
// ===============================

// Karena C++:
// - Bisa mengatur memori (RAM) secara langsung
// - Bisa bikin program lebih cepat
// - Memberi kontrol penuh ke programmer

// Cocok untuk program serius dan besar.


// ===============================
// Kenapa C++ sering diperbarui?
// ===============================

// Karena teknologi terus berkembang.

// Versi-versi C++:
// - C++11
// - C++14
// - C++17
// - C++20
// - C++23

// Tujuannya supaya:
// - Lebih mudah dipakai
// - Lebih aman
// - Lebih modern


// ===============================
// Mengapa menggunakan C++?
// ===============================

// 1. C++ sangat populer
// Banyak dipakai di:
// - Sistem operasi
// - Software besar
// - Program profesional

// 2. C++ itu cepat
// Kalau butuh program ngebut, C++ pilihan tepat.

// 3. C++ membantu belajar logika
// Belajar C++ bikin:
// - Cara berpikir lebih rapi
// - Mudah belajar bahasa lain

// 4. C++ bisa dipakai di banyak platform
// Satu kode bisa dijalankan di:
// - Windows
// - Linux
// - Mac

// 5. C++ memudahkan belajar bahasa lain
// Karena mirip dengan:
// - C
// - Java
// - C#

// Kalau sudah paham C++, belajar bahasa lain jadi lebih gampang.


// ===============================
// Perbedaan C dan C++
// ===============================

// C:
// - Lebih sederhana
// - Tidak ada class
// - Cocok untuk program kecil

// C++:
// - Lebih lengkap
// - Ada class dan object
// - Cocok untuk program besar

// Ibaratnya:
// C   = sepeda
// C++ = motor
// Motor punya fitur lebih banyak.


// ===============================
// Kesimpulan
// ===============================

// C++ adalah bahasa pemrograman yang digunakan untuk membuat berbagai
// aplikasi dengan performa tinggi. Bahasa ini dikembangkan dari C dan
// memberikan programmer kontrol yang besar terhadap penggunaan memori
// dan sumber daya sistem. C++ banyak digunakan karena cepat, kuat, dan
// dapat dijalankan di berbagai platform. Selain itu, C++ mendukung
// pemrograman berorientasi objek sehingga kode menjadi lebih terstruktur
// dan mudah dikembangkan. Oleh karena itu, C++ sangat cocok dipelajari
// sebagai dasar pemrograman dan sebagai langkah awal memahami bahasa
// pemrograman lain.
